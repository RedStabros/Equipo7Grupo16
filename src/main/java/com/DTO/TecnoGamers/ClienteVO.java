package com.DTO.TecnoGamers;

public class ClienteVO {
	private Long cedula_c;
	private String direccion_c;
	private String email_c;
	private String nombre_c;
	private String telefono_c;

	public Long getCedula_c() {
		return cedula_c;
	}
	public void setCedula_c(Long cedula_c) {
		this.cedula_c = cedula_c;
	}
	public String getDireccion_c() {
		return direccion_c;
	}
	public void setDireccion_c(String direccion_c) {
		this.direccion_c = direccion_c;
	}
	public String getEmail_c() {
		return email_c;
	}
	public void setEmail_c(String email_c) {
		this.email_c = email_c;
	}
	public String getNombre_c() {
		return nombre_c;
	}
	public void setNombre_c(String nombre_c) {
		this.nombre_c = nombre_c;
	}
	public String getTelefono_c() {
		return telefono_c;
	}
	public void setTelefono_c(String telefono_c) {
		this.telefono_c = telefono_c;
	}

}